#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>

/**
 * op_add - Entry Point
 * @a: int
 * @b: int
 * Description: add two integers)?
 * Return: integer
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Entry Point
 * @a: int
 * @b: int
 * Description: subtracts from two numbers)?
 * Return: integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Entry Point
 * @a: int
 * @b: int
 * Description: multiplies two numbers)?
 * Return: integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Entry Point
 * @a: int
 * @b: int
 * Description: gets number divided by number)?
 * Return: integer
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Entry Point
 * @a: int
 * @b: int
 * Description: gets remainder of two numbers)?
 * Return: integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
